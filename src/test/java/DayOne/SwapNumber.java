package DayOne;

public class SwapNumber {
    public static void main(String[] args) {
        int num1,num2,tmp;
        num1 = 10;
        num2 = 20;
        System.out.println("First Numeber is " +num1);
        System.out.println("Second Number is " +num2);
        tmp = num1;
        num1 = num2;
        num2 = tmp;
        System.out.println("After Program Run First Number is " +num1);
        System.out.println("After program run Second Number is " +num2);


    }
}
